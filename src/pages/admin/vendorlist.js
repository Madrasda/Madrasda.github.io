import Image from "next/image";
import Head from "next/head";
import Link from "next/link";
import jwt from "jsonwebtoken";
import axios from "axios";
import SearchVendor from "@/components/search-vendor";
import AdminLayout from "@/components/layout-admin";
import AddVendorModal from "@/components/addvendor-modal";
import VendorListItem from "@/components/vendorlist-item";
import { useEffect, useState } from "react";
import { useRouter } from "next/router";

export default function VendorList(props) {
  const router = useRouter();
  const [vendors, setVendors] = useState(null);

  const verifyToken = async () => {
    const url = new URLSearchParams({
      token: localStorage.getItem('token')
    })
    axios.get(
      "http://localhost:8080/api/auth/?" + url
    ).then((response) => {
      console.log("refreshed");
    }).catch((err) => {
      localStorage.removeItem("token");
      router.push("/admin");
    })
  }

  const getVendors = async () => {
    axios.get(
      "http://localhost:8080/api/admin/getVendors"
    ).then((response) => {
      setVendors(response.data);
    }).catch((err)=>{
      console.log(err);
    })
  };

  const handleSubmit = (formData) => {
      registerVendor(formData);
  }

  const registerVendor = async (data) => {
    axios.post(
      "http://localhost:8080/api/admin/addVendor",
      data
    ).then((response) => {
      console.log(response);
      getVendors();
    }).catch((err)=>{
      console.log(data);
      console.log(err);
    })
  };

  useEffect(() => {
    const token = localStorage.getItem("token");
    if (!token) {
      router.push("/admin");
    } else {
      try {
        verifyToken();
        getVendors();
      } catch (err) {
        router.push("/admin");
      }
    }
  }, []);

  return (
    <>
      <Head>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/logo.png" />
        <title>Madrasda | Vendor List</title>
      </Head>

      <AdminLayout>
        <section
          className="body-font overflow-hidden font-algeria
                        md:ml-32"
        >
          <div className="px-10 my-15 mx-auto">
            <h1 className="text-3xl text-primary 
                       md:ml-20 m-5">MY VENDORS</h1>
            <div className="flex flex-wrap md:ml-14">
              <div className="lg:w-1/6 md:w-1/2 p-4 w-4/6 lg:h-80 md:h-96 h-80 flex items-center justify-center m-5 rounded duration-200 ease-in-out">
                <Link href="#">
                  <div className="flex flex-col items-center justify-center cursor-pointer">
                    <AddVendorModal getFormData={handleSubmit} />
                    <p className="font-semibold text-base text-center">
                      Add new vendor
                    </p>
                    <p className="font-light text-gray text-sm text-center">
                      Add new vendors to your list
                    </p>
                  </div>
                </Link>
              </div>

              {vendors &&
                vendors.map((vendor) => (
                  <Link href={`/admin/vendorDetails/${vendor.id}`} className="lg:w-1/6 md:w-1/2 p-4 pb-0 min-h-full h-72 cursor-pointer bg-off-white m-5 rounded drop-shadow-[4px_4px_10px_rgba(0,0,0,0.2)] hover:drop-shadow-[8px_8px_4px_rgba(0,0,0,0.3)] duration-200 ease-in-out">
                    <VendorListItem
                      key={vendor.id}
                      vendorName={vendor.name}
                      image={vendor.imgUrl}
                    />
                  </Link>
                ))}
            </div>
          </div>
        </section>
      </AdminLayout>
    </>
  );
}
